    void GenerateGrid()
    {
        int rows = levelData.GetLength(0);
        int cols = levelData.GetLength(1);

        // --- LOGIC TÍNH TOÁN VỊ TRÍ VẪN GIỮ NGUYÊN ---
        // Nhưng bây giờ cellSize sẽ là đơn vị trong world space
        // Ví dụ: 1.0f tương ứng với kích thước của sprite (nếu Pixels Per Unit là 100)
        float totalWidth = cols * cellSize;
        float totalHeight = rows * cellSize;

        Vector2 startPos = new Vector2(
            -totalWidth / 2f + cellSize / 2f,
            totalHeight / 2f - cellSize / 2f
        );

        // Đặt vị trí cho GridContainer để căn giữa màn hình (tùy chọn)
        // gridContainer.position = new Vector3(0, 0, 0);

        for (int y = 0; y < rows; y++)
        {
            for (int x = 0; x < cols; x++)
            {
                int colorID = levelData[y, x];
                if (colorID == 0) continue;

                // Chú ý: transform.position (world space) thay cho transform.localPosition (UI space)
                // Tuy nhiên, vì gridContainer ở gốc (0,0,0), chúng hoạt động tương tự nhau
                GameObject pixelObject = Instantiate(pixelPrefab, gridContainer);
                pixelObject.transform.position = new Vector3(
                    startPos.x + x * cellSize,
                    startPos.y - y * cellSize,
                    0 // Đặt z = 0
                );

                PixelCell cell = pixelObject.GetComponent<PixelCell>();
                cell.Initialize(colorID);

                if (!cellsByColorID.ContainsKey(colorID))
                {
                    cellsByColorID[colorID] = new List<PixelCell>();
                }
                cellsByColorID[colorID].Add(cell);
            }
        }
    }
    void GeneratePalette()
    {
        // Bắt đầu lặp từ 1, vì gameColors[0] là màu trống, không cần tạo nút
        for (int i = 1; i < gameColors.Count; i++)
        {
            // Tạo nút mới từ prefab
            GameObject buttonObject = Instantiate(paletteButtonPrefab, paletteContainer);

            // Lấy script và gọi hàm Initialize để thiết lập
            PaletteButton paletteButton = buttonObject.GetComponent<PaletteButton>();
            paletteButton.Initialize(i, gameColors[i]);
        }
    }

    public void SelectColor(int colorID)
    {
        StopBlinking();

        selectedColorID = colorID;
        Debug.Log("Selected Color ID: " + selectedColorID);

        StartBlinking(selectedColorID);
    }

    public void TryColorCell(PixelCell cell)
    {
        if (selectedColorID != -1 && cell.colorID == selectedColorID)
        {
            Color colorToApply = gameColors[cell.colorID];

            colorToApply.a = 1f;

            // Dùng màu đã được đảm bảo để tô
            cell.SetColor(colorToApply);
        }
        else
        {
            Debug.Log("Wrong color!");
        }
    }
    public void StartDragColoring(PixelCell startingCell)
    {
        // Chỉ bắt đầu kéo khi màu chọn khớp với ô bắt đầu
        if (!startingCell.isColored && selectedColorID == startingCell.colorID)
        {
            isDraggingToColor = true;
            dragColorID = startingCell.colorID;
            // Tô màu ngay cho ô bắt đầu
            TryColorCell(startingCell);
        }
    }

    public void DragOverCell(PixelCell cell)
    {
        // Nếu đang trong chế độ kéo và ô này có màu khớp thì tô
        if (isDraggingToColor && !cell.isColored && cell.colorID == dragColorID)
        {
            TryColorCell(cell);
        }
    }

    public void StopDragColoring()
    {
        isDraggingToColor = false;
        dragColorID = -1; // Reset lại
    }

    private void StartBlinking(int colorID)
    {
        if (cellsByColorID.ContainsKey(colorID))
        {
            blinkingCoroutine = StartCoroutine(FadeCellsCoroutine(cellsByColorID[colorID]));
        }
    }

    private void StopBlinking()
    {
        if (blinkingCoroutine != null)
        {
            StopCoroutine(blinkingCoroutine);
            blinkingCoroutine = null;

            foreach (var list in cellsByColorID.Values)
            {
                foreach (var cell in list)
                {
                    cell.ResetHighlight();
                }
            }
        }
    }

    private IEnumerator FadeCellsCoroutine(List<PixelCell> cellsToHighlight)
    {
        List<PixelCell> uncoloredCells = cellsToHighlight.Where(c => !c.isColored).ToList();

        while (true)
        {
            // 1. Fade từ màu 1 sang màu 2
            float elapsedTime = 0f;
            while (elapsedTime < fadeDuration)
            {
                // Tính toán tỉ lệ t (từ 0 đến 1)
                float t = elapsedTime / fadeDuration;
                // Lấy màu được pha trộn tại thời điểm t
                Color currentColor = Color.Lerp(highlightColor1, highlightColor2, t);

                // Áp dụng màu này cho tất cả các ô cần làm nổi bật
                foreach (var cell in uncoloredCells)
                {
                    if (!cell.isColored)
                        cell.GetComponent<SpriteRenderer>().color = currentColor;
                }

                elapsedTime += Time.deltaTime;
                yield return null;
            }

            // 2. Fade ngược từ màu 2 về màu 1
            elapsedTime = 0f;
            while (elapsedTime < fadeDuration)
            {
                float t = elapsedTime / fadeDuration;
                Color currentColor = Color.Lerp(highlightColor2, highlightColor1, t);

                foreach (var cell in uncoloredCells)
                {
                    if (!cell.isColored)
                        cell.GetComponent<SpriteRenderer>().color = currentColor;
                }

                elapsedTime += Time.deltaTime;
                yield return null;
            }
        }
    }
