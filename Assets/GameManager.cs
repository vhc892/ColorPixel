    [SerializeField] private float fadeDuration = 1.2f;
    private readonly Color highlightColor1 = new Color(0.9f, 0.9f, 0.9f); // Xám rất nhạt
    private readonly Color highlightColor2 = new Color(0.65f, 0.65f, 0.65f); // Xám đậm hơn
